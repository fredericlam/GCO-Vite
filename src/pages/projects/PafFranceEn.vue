                  <h2>FAQ</h2>

                  	<div id="accordion">
						<h3>1.	Dans quel contexte ce projet a-t-il été réalisé? </h3>
						<div>
							<p>Depuis la dernière étude publiée sur la part des cancers attribuables aux facteurs liés au mode de vie et à l’environnement (2007), de nouveaux facteurs de risque de cancer ont été établis, l’exposition de la population aux facteurs de risque a changé, et de nouvelles données sont parues. Il était donc nécessaire de mettre à jour les estimations précédentes, afin de pouvoir communiquer sur l’impact des facteurs de risque de cancer sur la base d’informations scientifiques mises à jour. </p>
						</div>

						<h3>2.	Pourquoi estimer le nombre de cas de cancers pouvant être attribués à des facteurs liés au mode de vie ou à l’environnement ? </h3>
						<div>
							<p>Estimer le nombre de cas de cancers attribuables à ces facteurs de risque permet d’identifier ceux qui contribuent le plus au nombre de cas de cancers observés aujourd’hui. Des actions de prévention ciblées peuvent ensuite être mises en place pour faire diminuer l’exposition à ces facteurs de risque, et donc le nombre de cancers associé. Ce travail permet aussi d’informer le public sur l’importance relative des facteurs de risque. </p>
						</div>

						<h3>3.	Qui a réalisé cette étude ? </h3>
						<div>
						<p>Cette étude a été financée par l’Institut National du Cancer ; elle a été réalisée et coordonnée par le Centre International de Recherche sur le Cancer (CIRC). En parallèle, plus de 70 experts des principales institutions de recherche ou de santé publique françaises ont été impliqués (liste des contributeurs présentée dans l’annexe 4 du rapport) : ceci a permis d’assurer l’utilisation des meilleures méthodes et données disponibles. Des experts de ce type d’estimation à l’étranger ont également été impliqués pour donner des conseils méthodologiques et apporter leur perspective.  </p>
						</div>

						<h3>4.	Qu’est-ce qu’une fraction attribuable ? </h3>
						<div>
						<p>La fraction attribuable à des facteurs de risque représente approximativement la part des cancers qui aurait été évitée si la population n’avait pas été exposée à ces facteurs de risque. En multipliant la fraction attribuable par le nombre de cas de cancers observés dans la population, il est possible de donner un ordre d’idée du nombre de cas qui pourraient être évités en supprimant (ou en modifiant) l’exposition aux facteurs de risque. Dans notre étude par exemple, si personne ne fumait, 20% des cancers (soit plus de 68 000 cas) observés en 2015 ne seraient pas apparus. </p>
						</div>

						<h3>5.	Qu’est-ce qu’un facteur de risque de cancer?</h3>
						<div>
							<p>Un facteur de risque de cancer est tout attribut, caractéristique ou exposition d’un sujet qui augmente la probabilité de développer un cancer (par exemple, fumer ou boire des boissons alcoolisées sont des facteurs de risque de cancer). Certains facteurs, à l’inverse, font diminuer la probabilité de développer un cancer : il s’agit des facteurs dits protecteurs (par exemple, consommer suffisamment de fruits et de légumes, ou ne pas être en surpoids, sont des facteurs protecteurs). <br />
							Dans cette étude, nous nous sommes intéressés aux facteurs de risque et facteurs protecteurs liés au mode de vie et à l’environnement (voir ci-dessous). Nous n’avons en revanche pas inclus les facteurs héréditaires. </p>
						</div>

						<h3>6.	Qu’appelle-t-on facteurs liés au « mode de vie » ?</h3>
						<div>
							<p>Les facteurs liés au « mode de vie » sont l’ensemble des facteurs de risque de cancer liés au comportement individuel (qui peuvent être modifiés par des choix personnels) : la consommation de tabac, d’alcool, l’alimentation, l’activité physique, l’exposition aux cabines UV, sont des facteurs liés au mode de vie.  </p>
						</div>

						<h3>7.	Qu’appelle-t-on facteurs liés à l’« environnement » ? </h3>
						<div>
							<p>Dans notre étude, les facteurs liés à l’« environnement » sont l’ensemble des facteurs auxquels la population est exposée via différents milieux (air, eau, sols, etc…) : les agents infectieux, la pollution de l’air, les expositions professionnelles ou les substances chimiques de l’environnement par exemple, sont considérés comme des facteurs environnementaux. </p>
						</div>

						<h3>8.	Comment les facteurs de risque étudiés ont-ils été sélectionnés ? </h3>
						<div>
							<p>Les facteurs de risque étudiés ont été sélectionnés si leur cancérogénicité chez l’être humain était scientifiquement établie, c’est-à-dire s’ils étaient classés cancérogènes ou probablement cancérogènes par le CIRC. De plus, des données d’exposition représentatives de la population française devaient être disponibles pour chaque facteur de risque étudié. Ainsi par exemple, la contribution de l’exposition de la population générale aux pesticides au risque de cancer n’a pas pu être estimée, car aucune donnée d’exposition aux pesticides représentative n’existe à ce jour pour la population générale.  Enfin, seuls les facteurs de risque modifiables ont été pris en compte. </p>
						</div>

						<h3>9.	Comment sont calculés les chiffres présentés ? </h3>
						<div>
							<p>Les chiffres présentés sont basés sur trois principaux types de données : la proportion de la population française exposée à chaque facteur de risque (par exemple, pourcentage de fumeurs), la quantification du risque de cancer associé à chaque facteur de risque (par exemple, à combien s’élève le risque de cancer chez un fumeur comparé à celui d’un non-fumeur), et enfin le nombre de cancers observés en France en 2015 chez les personnes âgées de 30 ans ou plus. </p>
						</div>

						<h3>10.	Quelle est la différence entre les chiffres présentés dans ce rapport, et ceux présentés en 2007 par le CIRC ?  </h3>
						<div>
							<p>Les chiffres de la présente étude utilisent des données d’exposition et de risque mises à jour (dernières données disponibles). Les estimations qui ont été faites ici portent sur le nombre de cas de cancers observés en 2015 (estimé à partir des données des registres de cancer FRANCIM), alors qu’il s’agissait des chiffres pour l’année 2000 dans l’étude précédente. Enfin, les chiffres présentés ici prennent en compte un plus grand nombre de facteurs de risque : par exemple, l’alimentation et de nombreuses expositions professionnelles (gaz d’échappement diesel, par exemple) inclues ici ne l’avaient pas été en 2007. Par ailleurs, pour la présente étude, le CIRC a travaillé en collaboration avec l’ensemble des agences sanitaires et de recherche françaises, ce qui n’était pas le cas pour l’étude précédente. </p>
						</div>

						<h3>11.	Au total, combien de cas de cancers peut-on attribuer à des facteurs liés au mode de vie ou à l’environnement ? </h3>
						<div>
							<p>Chez les adultes de 30 ans et plus en France en 2015, on estime que 41% des cancers, soit environ 142 000 cas pourraient être évités si l’exposition de la population française aux facteurs liés au mode de vie et à l’environnement était optimale. Une exposition optimale correspond à une exposition nulle pour les facteurs de risque qui peuvent être évités (comme le tabac ou l’alcool), ou à une exposition limitée pour les autres, comme l’exposition au rayonnement UV par exemple. </p>
						</div>

						<h3>12.	Quels sont les facteurs de risque de cancer les plus importants en France ? </h3>
						<div>
							<p>Les premières causes de cancer en France sont les consommations de tabac et d’alcool, respectivement responsables de 20% et 8% des cancers. Viennent ensuite l’alimentation (consommation insuffisante de de fruits, légumes, fibres, produits laitiers, et consommation de viande rouge et charcuterie) (5,4%), le surpoids et l’obésité (5,4%), puis les agents infectieux (4%), les expositions professionnelles (3,6%) et les rayonnements ultraviolets (rayonnement solaire et cabines UV) (3%).  </p>
						</div>

						<h3>13.	Pourquoi la contribution de certains facteurs de risque est-elle si importante, alors que les niveaux d’exposition à certains facteurs de risque sont actuellement en diminution (tabagisme, exposition à l’amiante,…) ?</h3>
						<div>
							<p>Pour la majorité des facteurs de risque, une période de latence de 10 ans minimum a été prise en compte entre l’estimation des expositions et la survenue des cancers observés pour tenir compte du délai d’apparition de la maladie après l’exposition à risque. C’est pourquoi  des expositions passées peuvent encore avoir un impact aujourd’hui. C’est le cas par exemple des expositions professionnelles (exposition à des cancérogènes en milieu professionnel) : l’exposition à l’amiante, bien qu’interdite depuis 1997, a encore un impact majeur dans l’apparition de cas de cancers en 2015 ; c’est aussi la raison pour laquelle le tabac demeure la première cause de cancer en France, malgré la récente diminution de la consommation de tabac. </p>
						</div>

						<h3>14.	Pourquoi y a-t-il si peu de cas de cancers attribuables à l’exposition à des substances chimiques (pesticides, perturbateurs endocriniens) ?</h3>
						<div>
							<p>Nous avons tenté d’estimer la part de cancers attribuables aux substances chimiques cancérogènes présentes dans l’environnement général (les pesticides en sont un exemple). Pour la majorité de ces substances, aucune donnée d’exposition représentative et/ou aucune donnée de quantification du risque n’était disponible pour la population générale, ce qui rendait l’estimation impossible. Ce constat permet de souligner le besoin de davantage de données d’exposition et de quantification du risque correspondant. </p>
						</div>

						<h3>15.	Quelles sont les autres causes de cancers ? </h3>
						<div>
							<p>Nos résultats montrent que 41% des cancers pourraient être évités par la réduction de l’exposition de la population aux facteurs de risque étudiés. La part de cancers restante peut s’expliquer par des facteurs de risque qui n’ont pas été inclus dans l’analyse, faute de données disponibles (données d’exposition ou cancérogénicité non établie chez l’être humain), parce qu’ils sont non modifiables (nombre d’enfants, par exemple), ou encore par des facteurs de risque héréditaires, ou encore inconnus à ce jour. </p>
						</div>               	
